function maisForte (p1, p2) {
    console.log(`Comparação entre ${p1.codinome} e ${p2.codinome}`)

    let atributos = ["forca", "velocidade", "resistencia"];

    let vitoriasp1 = 0;
    let vitoriasp2 = 0;

    for (let atributo of atributos){
        if (p1[atributo] > p2[atributo]) {
            console.log(`${p1.codinome} vence em ${atributo}! (${p1[atributo]} vs ${p2[atributo]})`);
            vitoriasp1++;
        } else if (p1[atributo] < p2[atributo]) {
            console.log(`${p2.codinome} vence em ${atributo}! (${p2[atributo]} vs ${p1[atributo]})`);
            vitoriasp2++;
        } else {
            console.log(`Empate em ${atributo}! (${p1[atributo]} vs ${p2[atributo]})`);
        }
    }

    if (vitoriasp1 > vitoriasp2) {
        console.log(`${p1.codinome} venceu a batalha geral!`);
    } else if (vitoriasp2 > vitoriasp1) {
        console.log(`${p2.codinome} venceu a batalha geral!`);
    } else {
        console.log("Empate geral!");
    }
}
for (let i = 0; i < personagens.length; i++) {
    for (let j = i + 1; j < personagens.length; j++) {
        maisForte(personagens[i], personagens[j]);
    }
}